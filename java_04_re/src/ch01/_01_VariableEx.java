package ch01;

//AI서비스_웹과정반 @1일차
public class _01_VariableEx {
	public static void main(String[] args) {
		/**! 변수를 이용한 출력, 기본자료형 */
		/* 정보를 바꿔서 출력:변수명, 값 */
		// 연예인 정보

		String name = "우지호";
		System.out.println("이름: " + name);

		String birthday = "1992.09.14";
		System.out.println("생년월일: " + birthday);

		int height = 182;
		System.out.println("신장: " + height);

		int weight = 72;
		System.out.println("몸무게: " + weight + "kg");

		char bloodType = 'O';
		System.out.println("혈액형: " + bloodType);

		String religion = "천주교";
		System.out.println("종교: " + religion);

		String group = "블락비";
		System.out.println("소속그룹: " + group);
	}
}
